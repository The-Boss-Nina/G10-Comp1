# Teste de strings

# Strings simples
nome = "João"
sobrenome = 'Silva'
frase = "Olá, mundo!"

print(nome)
print(sobrenome)
print(frase)

# Strings com aspas
texto_aspas_duplas = "Ele disse: 'Olá!'"
texto_aspas_simples = 'Ela respondeu: "Oi!"'

print(texto_aspas_duplas)
print(texto_aspas_simples)

# Strings vazias
vazia_dupla = ""
vazia_simples = ''

print(vazia_dupla)
print(vazia_simples)